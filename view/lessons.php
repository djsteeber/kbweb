  <h3>Who</h3>
  <p>The program is open to all local residents. Children under 16
    must be accompanied by an adult who also participates in the class.
    The focus of the class is family archery and entire families are
    encouraged to participate together. The only limitation for our
    youngest participants is that they be mature enough to focus on the
    hour-and-a-half of class activity and strong enough to
    independently pull and hold our mini bows.
  </p>
  <h3>When</h3>
  <p>
    Family Archery classes meet 1 day a week for 3 weeks.
    <br /> Class sizes are limited, so please call or email early to insure a spot.
  </p>

