<h2>
  Membership
</h2>
<h3>
  The Kenosha Bowmen have an open membership policy.
</h3>
<h3>
  To become a member you must
  fulfill the following requirements:
</h3>
<ul>
<!--
  <li>
    Fill out an Online Membership Application ( 
  <a href="/index.php/membership/registration">
      Online Membership Registration
    </a>.) 
  </li>
-->
  <li>
    Fill out a Membership Application ( 
  <a href="/misc_docs/membership/MembershipApplication.pdf">
      Click here for the application form
    </a>.) 
  </li>
  <li>Pay a one time $100 Initiation Fee.
  </li>
  <li>Pay $60 annual dues.
  </li>
  <li>Pay $20 Wisconsin Bowhunters annual membership fee.
  </li>
  <li>Have your application signed by a member/sponsor.
  </li>
  <li>Attend a regular meeting to be voted into membership.
      (First Sunday of every month.)
  </li>
</ul>
<h3>
  As a member you have the following privileges:
</h3>
<ul>
  <li>Access to the front gate and clubhouse.
  </li>
  <li>Use of all our ranges and facilities for you and your immediate family.
  </li>
  <li>Members rates at local archery shoots.
  </li>
</ul>
<h2>
  As a member you have the following obligations:
</h2>
<ul>
  <li>Pay annual dues on time.
  </li>
  <li>Work at least 20 hours for the benefit of the club per year. 
      (Prorated for mid-year applicants)
  </li>
</ul>
