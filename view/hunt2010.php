<b>RD 10/25 -</b> Kirk and I each saw bucks on Sunday evening, but were unable to shoot because of earn-a-buck regulations. Mine was a small buck that was injured probably in a car accident. He was limping and had a broken antler. Kirk saw a large buck that was chased into the woods by neighboring 4 wheeler activity..
<p />
<b>VM 11/1 -</b> we hunted hard and full days.  Luckily, the weather was on our side, it was dry but a couple chilly mornings.  I saw the wounded the 7 pointer that Rick saw prior weekend on Thursday evening.  On Friday, I saw a 6 pointer at 1:10 in the afternoon.  Saturday and Sunday, we didn't see anything.  We hunted primarily on Ranges 1 & 3.  Not much to report - 

<p />
<b>MR 11/9 -</b> Mike and Squint hunted Thursday thru Sunday. Mike hunted in Ricks stand between range 1 and 2. Saw a 7pt. Saturday at 2:15,but no earn a buck tag. Squint saw a nice buck late Sunday near the northwest corner of range 1. No buck tag either.

<p />
<b>AC 11/13 -</b> I checked TC1 and it showed no activity since it had been moved to that location. Now you tell me!

<p />
<b>AC 11/16 -</b> Close encounter of the first kind was tracks near my blind Friday morning that weren't there Thursday. Close encounter of the second kind; I saw a shadowy image of a buck walking on the road Saturday at last light. Close encounter of the third kind? nope; Mark neither. Note to self; next time bring judo points and shoot the squirrels.

<p />
<b>AC 12/6 -</b> As we get ready for another round of bow seasons we're still hoping for our first kill. Jim and Cookie Peters are nest up. Cookie placed her tent style ground blind (GB2 on the map) in Range 4. There is plenty of evidence that deer are using a trail that crosses in front of her blind, but that might be all nocturnal. Jim is taking over TS 4 on the East side of Range 3.

<p />
<b>MH 12/20 -</b>  I have to tell you, this was my very first, legal, deer hunt.  The deer I killed with my car 2 years ago, doesn't count.  Can't tell you how proud I felt to pin that license on the back of my jacket and getting dressed in my camo pants and jacket. Friday night I was at Gander Mtn. buying warmer boots.   John hunted on Thurs afternoon and we were both out there Friday and Saturday afternoon.  John in the tree stand and me in the ground blind on Range 1.   Well, no deer appeared; it was cold and would I do it again? Yes I would. Was good being outdoors, seeing the light pattern changes as day went on and hearing nature's sounds.  Thanks to John for being so patient w/ this beginner.

<p />
<b>AC 12/22 -</b> Next up is Jeff Picard and Bryan Berger. I've moved the TS1 marker to indicate where I think Jeff is sitting. Jeff also mounted a trail cam there as well. I'll post any pictures that he gives me. He says that they'll probably spend some time on Range 3 as well. Let's hope they see some does they can shoot.

<p />
<b>JP 12/27 -</b> Didn't see one deer all four days, Brian hunted range 3 and I hunted range 1. There where fresh tracks of at least two or three deer going by my ground blind each day. Problem was that they were all going by during the night. (I will send you some trail cam pictures (TC 3)I have of the two does that seem to be traveling together.) There was also a single deer with a larger track (no pictures) that I would hope is still one of the bucks that was seen earlier in the season. Brian had tracks on range 3 each day too but I would assume that these were also going through at night as he didnt see anything either.
<p />

 
<b>DR 1/3 -</b> We were out Thurs night for a scouting night while we still had snow. Most of the tracks were between Ranges 4 and 1. It looked like they were going from field to field east to west and visa versa. We were out early Friday morning but didn't see anything, then rain came in so we called it quits. We came back out Friday night around 2:30. I was in my stand pulling up the bow which got stuck in junk around the base of the tree stand (a lesson learned!!) I finally got the bow up and turned around and to my surprise there were 3 does staring at me 15 yd. away. What could I do but watch them watch me. They turned around and ran back where they came from. That's all we saw Friday. We didn't hunt Saturday; too much new years!!!! We hunted Sunday afternoon from 2:00 - 5:00 but saw nothing. 
