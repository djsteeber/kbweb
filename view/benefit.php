<h3>Benefit Background</h3>
In 2009, one of our club members suffered a tragic loss in his immediate family.  Heide Jo Storbeck pass away unexpectedly on July 26, 2009, as St. Mary's Hospital due to an aortic aneurysm.  Heidi was 32 years young and left behind a loving family and many dear friends.  As a result, the Kenosha Bowmen sprung into action by hosting a rendezvous and rallied support from local agencies in order to raise money to aid this member in his time of need.
<p/>
Each year Kenosha Bowmen hosts a rendezvous benefit in Heide's honor.  
The event consists of a 3D shoot, live and silent auction, picnic, music and activities for all ages.

<p/>
This year's event will support the Bill Jecevicus Family.
<p/>
<h3>Bill Jecevicus &amp; Family</h3>

Bill was diagnosed with Stage IV appendix cancer in late Feburary.  His is being treated agressively at St. Catherine's Hospital and Froedtert Health to help slow the progression of this rare form of cancer.  Debbie, Bill's loving wife of 17 years and two daughters, Ashley (16), and Alyssa (13) would be honored if you could join us in helping them in their time of need.  Your attendance and support will go a long way toward assisting this wonderful family.
