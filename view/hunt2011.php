<h3>Log</h3>
<b>DW - 10/05/2011 - </b> 
The first week of the Super Hunt was unsuccessful.  The first couple days we had significant wind which made it difficult to hear anything or stay in the stand for that matter.  No deer were observed on the property over this four day hunt.  My guess is that when the surrounding corn fields are cut, we will see a significant increase in deer activity on our property.
<p/>

<b>JP - 10/31/2011 - </b> 
Still no deer to report for these two weeks as the weather seemed to still be to warm to get the deer moving. From what I was told by the hunters on these two weeks there was nothing really to report. I do have some deer on the trail cams I will try to post but everything is still moving during the dark. Look for the week for report coming soon, there may be some more encouraging news. -- Jeff
