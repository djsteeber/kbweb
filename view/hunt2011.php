<b>VM - 11/09/2011 - </b> 
It was fairly quiet.  I hunted on Thursday afternoon, didn't go out in morning due to rain, and Friday - pretty much all day and didn't see anything.  It was a nice sunny day, so good for sitting.  Jimmy Dorr and I hunted on Saturday morning and didn't see anything.  On Saturday morning they started working in the corn field and taking the corn down.  We were hopeful...so we sat out a little early on Saturday afternoon.  Jimmy sat on Range 4 near the pond and didn't see anything.  The four days that I hunted - I sat on Range 1 in the corner where the two stands are.  Around 4ish, a small buck was in the field and eventually came into our woods - behind me.  He walked down the range trail towards me and then geared off.  I drew back but didn't get a shot.  He was not a big buck in size or rack - a medium size.  His rack was dark brown and went straight up with short tines - maybe a 6 pointer.  I wasn't discouraged not having a shot because of his size.
 
On Sunday morning, it was another beautiful day - Jimmy sat on Range 1 and didn't see anything.  He sat on Range 2 in the afternoon and didn't see anything.  Sunday morning - I spotted an 8-pointer in the field, around 6:15-6:30am, and eventually he came in behind me.  Walked over to the range trail...I drew back on him twice.  I took a shot and missed him - my arrow went under him.  He was not a massive buck in antlers, but nice - I'm guessing about the same medium size. I found my arrow.  On Sunday afternoon, about 20 minutes before sunset, a four-pointer showed up, he was about 25 yards away but over the fence line in the other property.  I watched him till it was time to climb down and head back.
<br/><br/> 
I was optimistic - with rut starting and the corn down, I was hoping we would have better luck in the latter days.  I used a scented drag and bleated.  The two bucks that came in behind me - didn't pay attention to the bleating.  None of them had seen, heard or smelled me.  Jimmy tried several calls as well and nothing worked.  We had the time change too - but didn't seem to make any difference. 
<br/><br/> 
With the corn down and rut starting to really pick up now - I wish the next team luck and happy hunting!
